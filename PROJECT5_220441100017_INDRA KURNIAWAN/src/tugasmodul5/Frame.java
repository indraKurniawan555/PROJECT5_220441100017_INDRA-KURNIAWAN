    private void bCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCekActionPerformed
        // TODO add your handling code here:
         int jumlahPembelian = Integer.parseInt(tfJumlah_pembelian.getText());
    
    // Mendapatkan nilai harga barang dari input atau dari database jika Anda ingin mengambilnya dari database
    int hargaBarang = Integer.parseInt(tfHarga_Barang.getText());
    
    // Menghitung total harga
    int totalHarga = jumlahPembelian * hargaBarang;
    
    // Menampilkan total harga pada input total harga
    tfTotalHarga.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_bCekActionPerformed

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        int totalBayar = Integer.parseInt(tfTotal_Bayar.getText());
        int totalHarga = Integer.parseInt(tfTotalHarga.getText());
        int kembalian = totalBayar - totalHarga;
        if (kembalian < 0){
            JOptionPane.showMessageDialog(this, "Uang yang anda bayarkan kurang");
        }else {
        tfKembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_btnBayarActionPerformed

