    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
                                               
    
    int selectedRow = tabelsimpan.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
    } else {
        DefaultTableModel model = (DefaultTableModel) tabelsimpan.getModel();
        
        
        String kodeBarang = model.getValueAt(selectedRow, 1).toString();
        
        
        try {
            Statement st = conn.getConnection().createStatement();
            st.executeUpdate("DELETE FROM barang WHERE kode_barang = '" + kodeBarang + "'");
            st.close();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            
            
            model.removeRow(selectedRow);
            bersihkan();
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }

    //Dropdown.removeAllItems();
    tambahDropdown();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (isEditMode) {
        
        try {
            Statement st = conn.getConnection().createStatement();
            st.executeUpdate("UPDATE barang SET nama_barang='" + tfNamaBarang.getText() + "', kode_barang='"
                    + tfKodeBarang.getText() + "', harga_barang='" + tfHargaBarang.getText() + "' WHERE kode_barang='"
                    + tfKodeBarang.getText() + "'");
            st.close();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah");
            tampilData();
            bersihkan();
            tambahDropdown();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
        
        isEditMode = false;
        btnSimpan.setText("SIMPAN");
    } else {
        
        try {
            Statement st = conn.getConnection().createStatement();
            st.executeUpdate("INSERT INTO barang VALUES('" + tfNamaBarang.getText() + "', '"
                    + tfKodeBarang.getText() + "', '" + tfHargaBarang.getText() + "')");
            st.close();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            tampilData();
            bersihkan();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }
        //Dropdown.removeAllItems();
        
        tambahDropdown();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void DropdownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropdownActionPerformed
        // TODO add your handling code here:
       String selectedBarang = Dropdown.getSelectedItem().toString();

    try {
        
        Statement st = conn.getConnection().createStatement();
        String query = "SELECT kode_barang, harga_barang FROM barang WHERE nama_barang = '" + selectedBarang + "'";
        ResultSet rs = st.executeQuery(query);

        if (rs.next()) {
            
            tfKode_Barang.setText(rs.getString("kode_barang"));
            tfHarga_Barang.setText(rs.getString("harga_barang"));
        } else {
            
            tfHarga_Barang.setText("");
            tfKode_Barang.setText("");
        }

        rs.close();
        st.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_DropdownActionPerformed

    private void tabelsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelsimpanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelsimpan.getModel();
    int selectedRow = tabelsimpan.getSelectedRow();

    
    if (selectedRow >= 0) {
        tfNamaBarang.setText(model.getValueAt(selectedRow, 0).toString());
        tfKodeBarang.setText(model.getValueAt(selectedRow, 1).toString());
        tfHargaBarang.setText(model.getValueAt(selectedRow, 2).toString());
        isEditMode = true; 
        btnSimpan.setText("EDIT"); 
    }
    
    }//GEN-LAST:event_tabelsimpanMouseClicked

