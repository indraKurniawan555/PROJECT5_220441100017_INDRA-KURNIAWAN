    public void tambahDropdown() {
    try {
        Statement st = conn.getConnection().createStatement();
        ResultSet rs = st.executeQuery("SELECT nama_barang FROM barang");
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("Pilih Barang"); 
        while (rs.next()) {
            String namaBarang = rs.getString("nama_barang");
            model.addElement(namaBarang);
        }
        Dropdown.setModel(model);
        st.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }

    }



      public void tampilData(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("barang");
        tbl.addColumn("kode");
        tbl.addColumn("harga");
        
        tabelsimpan.setModel(tbl); 
        try {
            Statement st = conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM barang");
            while (rs.next()) {
                tbl.addRow( new Object[] {
                    rs.getString("nama_barang"),
                    rs.getString("kode_barang"),
                    rs.getString("harga_barang")
                });
                tabelsimpan.setModel(tbl);
                //Dropdown.addElement(rs.getString("nama_barang"));
            }
        } catch (Exception e) {
        }
    }
    
